/**
 * Class to show examples of division
 * @author Udayan Khattry
 */
public class _04Division {
    public static void main(String[] args) {
        int i1 = 12;
        int i2 = 5;
        float f1 = 5.0f;
        double d1 = 5.0;

        int res1 = i1 / i2;
        float res2 = i1 / f1;
        double res3 = i1 / d1;

        System.out.println("res1 = " + res1);
        System.out.println("res2 = " + res2);
        System.out.println("res3 = " + res3);
    }
}
