/**
 * Class to show examples of Unary Plus/ Minus
 * @author Udayan Khattry
 */

public class _06UnaryPlusMinus {
    public static void main(String[] args) {
        int i1 = +10; // optional, numbers are positive w/o this
        int i2 = 10;
        int i3 = -100; // "-" makes it a negative number
        int i4 = -(i1 + i2);

        System.out.println("i1 = " + i1);
        System.out.println("i2 = " + i2);
        System.out.println("i3 = " + i3);
        System.out.println("i4 = " + i4);

    }
}
